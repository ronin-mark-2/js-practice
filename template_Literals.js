// Before ES6 - Without template literals
var greet = "Hello";
var place = "World";
console.log(greet + " " + place + "!"); // display both varibles without using template literals


// After ES6 - With template literals
let greet = "Hello";
let place = "World";
console.log(`${greet} ${place} !`); // display both variables using template literals


// This syntax is good (Multi-line string)
`
Hello,
World
!
`

